    private function cosine($a, $b)
    {
        $dot = $magA = $magB = 0.0;
        $n = min(count($a), count($b));
        for ($i = 0; $i < $n; $i++) {
            $dot += $a[$i] * $b[$i];
            $magA += $a[$i] ** 2;
            $magB += $b[$i] ** 2;
        }
        return $dot / (sqrt($magA) * sqrt($magB));
    }

    protected function getAiAnswer($topResults, $query)
    {
        try {
            $context = $topResults->pluck('text')->implode("\n\n");
            
            $resp = OpenAI::chat()->create([
                'model' => 'gpt-4-turbo',
                'messages' => [
                    ['role' => 'system', 'content' => 'Answer concisely based only on the provided context. If the answer is not in the context, say "I don\'t have enough information to answer that question."'],
                    ['role' => 'user', 'content' => "Context:\n{$context}\n\nQuestion: {$query}"],
                ],
                'temperature' => 0.3,
                'max_tokens' => 1000,
            ]);

            return $resp['choices'][0]['message']['content'];
        } catch (\Exception $e) {
            logger()->error('AI answer error: ' . $e->getMessage());

            return "I encountered an error while generating an answer. Please try again.";
        }
    }

    /**
     * Generate a vector embedding for a given text using OpenAI.
     *
     * @param  string  $text  Input text.
     * @return array<float> Embedding vector values (empty array on failure).
     */
    public function generateEmbedding(string $text): array
    {
        $values = [];

        try {
            $result = OpenAI::embeddings()->create([
                'model' => 'text-embedding-3-large',
                'input' => $text,
            ]);

            $values = $result['data'][0]['embedding'];
        } catch (\Exception $e) {
            Log::info('Error in AIService->generateEmbedding', ['error' => $e->getMessage()]);
        }

        return $values;
    }

    /**
     * Fast text-based search without AI processing.
     * Uses database LIKE queries for instant results.
     *
     * @param  string  $question  The user's search query.
     * @return array<string,mixed> [
     *                             'text'    => string Search results,
     *                             'sources' => array<int,array{id:int,type:string}> List of matched sources
     *                             ]
     */
    public function fastSearch(string $question): array
    {
        // Try cache first for common queries
        $cacheKey = 'fast_search_'.md5(strtolower(trim($question)).'_'.(Auth::id() ?? 'guest'));
        $cached = Cache::get($cacheKey);

        if ($cached) {
            return $cached;
        }

        try {
            // -----------------------------------------
            // STEP 1: Extract keywords from question
            // -----------------------------------------
            $keywords = $this->extractKeywords($question);

            if (empty($keywords)) {
                return [
                    'text' => 'Please ask a more detailed question.',
                    'sources' => []
                ];
            }

            // -----------------------------------------
            // STEP 2: Load all text sections
            // -----------------------------------------
            $sections = [];

            $pageQuery = ArticlePage::where(function ($q) use ($question) {
                $q->where('native_text', 'ILIKE', "%{$question}%")
                    ->orWhere('ocr_text', 'ILIKE', "%{$question}%");
            });

            foreach ($keywords as $keyword) {
                $pageQuery->orWhere(function ($q) use ($keyword) {
                    $q->where('native_text', 'ILIKE', "%{$keyword}%")
                        ->orWhere('ocr_text', 'ILIKE', "%{$keyword}%");
                });
            }

            $values = $pageQuery->take(1)->get();
            $articleId = null;

            foreach ($values as $value) {
                if($articleId) continue;

                $articleId = $value->article_id;
            }

            $article = Article::findOrFail($articleId);

            if(!$article) {
                return [
                    'text' => 'No article content available.',
                    'sources' => []
                ];
            }

            $article->load(['pages', 'pages.images', 'pages.embeddings']);
            $best = collect();
            
            // First pass: collect all sections and their scores
            $formattedText = '';

            foreach ($article->pages ?? [] as $page) {
                $text = trim(($page->native_text ?? '') . "\n" . ($page->ocr_text ?? ''));
                $text = preg_replace('/\s+/', ' ', $text);
                if (empty($text)) continue;
                
                // Add the section text
                $formattedText .= $text;
            }

            $best->push([
                'type' => 'ArticlePage',
                'text' => $formattedText,
                'doc_id' => $page->id,
            ]);

            // -----------------------------------------
            // STEP 5: Ask GPT to answer using context only
            // -----------------------------------------
            $response = $this->getAiAnswer($best->values(), $question);

            return [
                'text' => $response,
                'sources' => []
            ];

        } catch (\Exception $e) {
            \Log::error('FAST SEARCH error', [
                'error' => $e->getMessage()
            ]);

            return [
                'text' => 'Something went wrong.',
                'sources' => []
            ];
        }
    }

    /**
     * Extract meaningful keywords from a search query.
     *
     * @return array<string>
     */
    private function extractKeywords(string $question): array
    {
        // Remove common stop words
        $stopWords = ['the', 'a', 'an', 'and', 'or', 'but', 'in', 'on', 'at', 'to', 'for', 'of', 'with', 'by', 'is', 'are', 'was', 'were', 'be', 'been', 'have', 'has', 'had', 'do', 'does', 'did', 'will', 'would', 'could', 'should', 'may', 'might', 'can', 'what', 'where', 'when', 'why', 'how', 'who', 'which'];

        // Clean and split the question
        $words = preg_split('/\s+/', strtolower(trim($question)));
        $keywords = [];

        foreach ($words as $word) {
            // Remove punctuation and keep only words with 3+ characters
            $word = preg_replace('/[^\w]/', '', $word);
            if (strlen($word) >= 3 && ! in_array($word, $stopWords)) {
                $keywords[] = $word;
            }
        }

        return array_unique($keywords);
    }

    /**
     * Highlight keywords in text for better readability.
     *
     * @param  array<string>  $keywords
     */
    private function highlightKeywords(string $text, array $keywords): string
    {
        // Limit text length for readability
        if (strlen($text) > 300) {
            // Try to find a good excerpt around the first keyword
            foreach ($keywords as $keyword) {
                $pos = stripos($text, $keyword);
                if ($pos !== false) {
                    $start = max(0, $pos - 100);
                    $text = substr($text, $start, 300);
                    break;
                }
            }
            if (strlen($text) > 300) {
                $text = substr($text, 0, 300);
            }
        }

        // Simple highlighting (you can enhance this)
        foreach ($keywords as $keyword) {
            $text = preg_replace('/('.preg_quote($keyword, '/').')/i', '**$1**', $text);
        }

        return $text;
    }

    /**
     * Answer a user's question using retrieval-augmented generation (RAG).
     *
     * Workflow:
     *  1. Generate an embedding for the question.
     *  2. Search the database for the most relevant embeddings using pgvector.
     *  3. Collect context text from matching ArticlePages and ArticleImages.
     *  4. Send the question + context to GPT for an answer.
     *
     * @param  string  $question  The user's natural language question.
     * @return array<string,mixed> [
     *                             'text'    => string Answer generated by GPT,
     *                             'sources' => array<int,array{id:int,type:string}> List of matched sources
     *                             ]
     */
    public function answerQuestion(string $question): array
    {
        try {
            // Validate OpenAI API key
            if (empty(config('openai.api_key'))) {
                Log::error('OpenAI API key is not configured');
                return [
                    'text' => 'AI service is not configured. Please contact the administrator.',
                    'sources' => []
                ];
            }

            // Generate embedding for the question
            $qEmbedding = $this->generateEmbedding($question);
            // $qEmbedding = $this->embArray();

            if (empty($qEmbedding)) {
                Log::error('Failed to generate embedding for question');
                return [
                    'text' => 'Unable to process your question. Please try again.',
                    'sources' => []
                ];
            }

            // Build the embedding query
            $qEmbeddingStr = '['.implode(',', $qEmbedding).']';
            $query = Embedding::selectRaw(
                'id, embeddable_id, embeddable_type, embedding <-> ? as distance',
                [$qEmbeddingStr]
            )->orderBy('embeddable_id', 'asc');

            // Apply access control for authenticated users
            if (Auth::check()) {
                $user = Auth::user();
                $query->whereHasMorph(
                    'embeddable',
                    [ArticlePage::class, ArticleImage::class],
                    function ($q) use ($user) {}
                );
            }

            // Get top relevant results
            $results = $query->with('embeddable')->get();

            if ($results->isEmpty()) {
                return [
                    'text' => 'No relevant information found. Please upload documents related to your question.',
                    'sources' => []
                ];
            }

            // Process results and build context
            $sources = [];
            $context = [];

            foreach ($results as $embedding) {
                $embeddable = $embedding->embeddable;
                if (!$embeddable) continue;

                $sources[] = [
                    'id' => $embeddable->id,
                    'type' => class_basename($embeddable)
                ];

                $content = '';
                if ($embeddable instanceof ArticlePage) {
                    $content = trim(($embeddable->native_text ?? '').' '.($embeddable->ocr_text ?? ''));
                } elseif ($embeddable instanceof ArticleImage) {
                    $content = trim($embeddable->ocr_text ?? '');
                }

                if (!empty($content)) {
                    $context[] = $content;
                }
            }

            $context = collect($context)->map(fn($text) => ['text' => preg_replace('/\s+/', ' ', trim($text))]);

            if (empty($context)) {
                return [
                    'text' => 'No text content found in the uploaded documents.',
                    'sources' => []
                ];
            }

            // Generate AI response
            $response = $this->getAiAnswer(
                $context,
                $question
            );

            return [
                'text' => $response ?: 'Unable to generate a response.',
                'sources' => $sources
            ];
        } catch (\OpenAI\Exceptions\ErrorException $e) {
            Log::error('OpenAI API Error in answerQuestion', [
                'error' => $e->getMessage(),
                'question' => $question,
                'trace' => $e->getTraceAsString()
            ]);
            return [
                'text' => 'AI service is temporarily unavailable. Please try again later.',
                'sources' => []
            ];
        } catch (\Exception $e) {
            Log::error('Error in AIService->answerQuestion', [
                'error' => $e->getMessage(),
                'question' => $question,
                'trace' => $e->getTraceAsString()
            ]);
            return [
                'text' => 'An error occurred while processing your question.',
                'sources' => []
            ];
        }
    }

